package com.gtdev5.geetolsdk.mylibrary.beans;

public class Nads {
    private String code;
    private String name;
    private String number;
    private String desc;
    private int type;
    private int ips;
    private String urlPoint;
    private String urlResources;
    private String remark;
    public void setCode(String code) {
        this.code = code;
    }
    public String getCode() {
        return code;
    }

    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }

    public void setNumber(String number) {
        this.number = number;
    }
    public String getNumber() {
        return number;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
    public String getDesc() {
        return desc;
    }

    public void setType(int type) {
        this.type = type;
    }
    public int getType() {
        return type;
    }

    public void setIps(int ips) {
        this.ips = ips;
    }
    public int getIps() {
        return ips;
    }

    public void setUrlPoint(String urlPoint) {
        this.urlPoint = urlPoint;
    }
    public String getUrlPoint() {
        return urlPoint;
    }

    public void setUrlResources(String urlResources) {
        this.urlResources = urlResources;
    }
    public String getUrlResources() {
        return urlResources;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
    public String getRemark() {
        return remark;
    }
}
